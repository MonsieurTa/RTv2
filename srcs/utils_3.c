#include <stdlib.h>
#include "rtv1.h"

int			is_v3(char **split)
{
	int	i;

	i = 0;
	while (split[i] != NULL)
	{
		if (is_float(split[i]) == 0 && is_number(split[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
